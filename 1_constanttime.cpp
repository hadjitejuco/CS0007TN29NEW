//Constant Complexity (O(1))
//This complexity class indicates that the operation does not depend on the input size.
#include <iostream>
using namespace std;

void constantComplexity() {
    int fixedOperation = 6 + 2; // A simple constant time operation
    cout << "Result: " << fixedOperation << endl;
}

int main() {
    constantComplexity();
    return 0;
}
